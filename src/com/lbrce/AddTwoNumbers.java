package com.lbrce;

public class AddTwoNumbers {
    public static void main(String[] args) {
        // Initialize the numbers
        int number1 = 1;
        int number2 = 2;

        // Calculate the sum
        int sum = number1 + number2;

        // Display the result
        System.out.println("The sum of " + number1 + " and " + number2 + " is: " + sum);
    }
}
